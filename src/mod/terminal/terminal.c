uint64_t frame_buffer_width;
uint64_t frame_buffer_height;
uint32_t *terminal_buffer_addr;
uint64_t terminal_width;
uint64_t terminal_height;
size_t current_row;
size_t current_col;
size_t cursor_row;
size_t cursor_col;
uint32_t bg_color;
uint32_t fg_color;

void terminal_init(void *addr, const uint64_t width, const uint64_t height) {
    terminal_buffer_addr = (uint32_t *)addr;
    frame_buffer_width = width;
    frame_buffer_height = height;
    terminal_width = width/8;
    terminal_height = height/16;
    current_row = 0;
    current_col = 0;
    bg_color = 0x000000;
    fg_color = 0xFFFFFF;
    cursor_row = 0;
    cursor_col = 0;
    set_cursor(current_row, current_col, 0);
}

size_t strlen(char* str) {
    size_t size = 0;
    while(str[size])
    {
        ++size;
    }
    return size;
}

void set_cursor(int row, int col, int remove) {
    //check if the position goes out of the buffer
    uint64_t offset = row * terminal_width + col;
    if(offset >= terminal_width * terminal_height)
    {
        return;
    }

    //! USE OF GOTO TO SKIP THE REMOVE CURSOR PART IF NOT TOLD SO
    if(!remove) {
        goto render;
    }

    //remove the cursor
    for(int i = 0; i < 16; ++i)
    {
        for(int j = 0; j < 8; ++j)
        {
            //location -> pixel
            size_t pixel_offset = (cursor_row * 16 + i) * frame_buffer_width + cursor_col * 8 + j;
            *(uint32_t *)(terminal_buffer_addr + pixel_offset) = bg_color;
        }
    }

    render:
    //render the cursor
    for(int i = 0; i < 16; ++i)
    {
        for(int j = 0; j < 8; ++j)
        {
            //location -> pixel
            size_t pixel_offset = (current_row * 16 + i) * frame_buffer_width + current_col * 8 + j;
            *(uint32_t *)(terminal_buffer_addr + pixel_offset) = 0xFFFFFF;
        }
    }
    cursor_row = current_row;
    cursor_col = current_col;
}

void terminal_advance() {
    if(++current_col == terminal_width)
    {
        current_col = 0;
        ++current_row;
    }
}

void terminal_putchar(char character) {
    //get the character
    size_t font_offset = character * 16;
    
    //render the character
    for(int i = 0; i < 16; ++i)
    {
        for(int j = 0; j < 8; ++j)
        {
            //location -> pixel
            size_t pixel_offset = (current_row * 16 + i) * frame_buffer_width + current_col * 8 + j;

            //if the bit is 1, then it's fg, otherwise bg
            if(((builtin_font[font_offset + i] >> (7 - j)) & 0x1) == 0x1) {
                *(uint32_t *)(terminal_buffer_addr + pixel_offset) = fg_color;
            } else {
                *(uint32_t *)(terminal_buffer_addr + pixel_offset) = bg_color;
            }
        }
    }

    terminal_advance();
}

void printf(char *format) {
    for(size_t i = 0; i < strlen(format); ++i)
    {
        terminal_putchar(format[i]);
    }
    set_cursor(current_row, current_col, 0);
}